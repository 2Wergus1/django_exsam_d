# Detail Page – bu foydalanuvchiga biror ma’lumotning to‘liq tafsilotini ko‘rsatadigan sahifa. Masalan, blogdagi har bir post uchun alohida sahifa ochiladi, u yerda faqat shu postga oid sarlavha, matn, muallif, sana va boshqa ma’lumotlar ko‘rsatiladi.
#
# Bu sahifalar odatda bitta obyekt (masalan, bir dona mahsulot yoki yangilik) asosida ishlaydi. Foydalanuvchi ro‘yxatdan birini tanlaganida, shu sahifa ochiladi.
